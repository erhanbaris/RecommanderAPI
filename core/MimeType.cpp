core::MimeType::MimeType() {
    initMimeTypes();
}

STR_TYPE core::MimeType::GetMimeType(STR_TYPE const &extension) {
    if (mimeTypes.find(extension) != endMimeType)
        return mimeTypes.find(extension)->second;

    return STR("text/html");
}

void core::MimeType::AddMimeType(STR_TYPE const &extension, STR_TYPE const &mime) {
    mimeTypes[extension] = mime;
    endMimeType = mimeTypes.end();
}

